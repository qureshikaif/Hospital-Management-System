    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String firstname = firstName.getText();
        String lastname = lastName.getText();
        String gender = buttonGroup1.getSelection().getActionCommand();
        String doctor = (String) doctorField.getSelectedItem();
        String contact = contactField.getText(); 
        int age = Integer.parseInt(ageField.getText()); 
        String address = addressField.getText();
        String description = diseaseField.getText(); 
        String email = emailField.getText(); 

    try {
    String dbURL = "jdbc:ucanaccess://C:\\Users\\LENOVO\\OneDrive\\Documents\\NetBeansProjects\\Hospital-Management-System\\HMS.accdb";
    // Establish a database connection
    Connection connection = DriverManager.getConnection(dbURL);

    // Retrieve user by email
    String selectQuery = "SELECT * FROM Patients WHERE Email = ?";
    PreparedStatement selectStatement = connection.prepareStatement(selectQuery);
    selectStatement.setString(1, email);
    ResultSet resultSet = selectStatement.executeQuery();

    if (resultSet.next()) {
    

        // Prepare SQL statement for updating patient details
        String updateQuery = "UPDATE Patients SET FirstName = ?, LastName = ?, Gender = ?, Doctor = ?, Contact = ?, Age = ?, Address = ?, Description = ? WHERE Email = ?";
        // Set values for prepared statement parameters
        try (PreparedStatement updateStatement = connection.prepareStatement(updateQuery)) {
            // Set values for prepared statement parameters
            updateStatement.setString(1, firstname);
            updateStatement.setString(2, lastname);
            updateStatement.setString(3, gender);
            updateStatement.setString(4, doctor);
            updateStatement.setString(5, contact);
            updateStatement.setInt(6, age);
            updateStatement.setString(7, address);
            updateStatement.setString(8, description);
            updateStatement.setString(9, email); // Update based on the email
            // Execute the update statement
            int rowsUpdated = updateStatement.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Appointment booked successfully");
            }
            // Close the update statement
        }
    } 
    else {
        JOptionPane.showMessageDialog(this, "User with specified email does not exist");
    }

    // Close the result set, select statement, and database connection
    resultSet.close();
    selectStatement.close();
    connection.close();
    } 
    catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Please fill all required fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

